/**
 * 转义 HTML 特殊字符
 */
function escapeHTML(text: string): string {
  const map: Record<string, string> = {
    '&': '&amp;',
    '<': '&lt;',
    '>': '&gt;',
    '"': '&quot;',
    "'": '&#039;'
  }
  return text.replace(/[&<>"']/g, m => map[m])
}

/**
 * 从 URL 获取页面内容并导出为单页 HTML
 * 
 * @param urls 页面 URL 数组
 * @param output 输出文件路径
 * @param options 配置选项
 * @returns Promise<void>
 * 
 * @example
 * ```ts
 * await exportURLsToSinglePage(
 *   [
 *     'http://localhost:5173/guide/intro',
 *     'http://localhost:5173/guide/setup'
 *   ],
 *   './output/guide.html',
 *   {
 *     title: 'User Guide',
 *     inlineStyles: true
 *   }
 * )
 * ```
 */
export async function exportURLsToSinglePage(
  urls: string[],
  output: string,
  options: Partial<SinglePageExportOptions> = {}
): Promise<void> {
  // 动态导入 Playwright
  let playwright: any
  try {
    playwright = await import('playwright')
  } catch (error) {
    throw new Error(
      'Playwright is not installed. Please install it with: npm install -D playwright'
    )
  }

  const browser = await playwright.chromium.launch({ headless: true })
  const pages: PageContent[] = []

  try {
    const context = await browser.newContext()

    for (const url of urls) {
      const page = await context.newPage()

      try {
        await page.goto(url, { waitUntil: 'networkidle' })

        // 提取页面标题和内容
        const pageData = await page.evaluate(() => {
          const titleEl = document.querySelector('h1') || document.querySelector('title')
          const title = titleEl?.textContent?.trim() || 'Untitled'

          // 获取主要内容区域
          const contentEl = document.querySelector('.vp-doc') || document.querySelector('main') || document.body
          const content = contentEl?.innerHTML || ''

          return { title, content }
        })

        pages.push({
          title: pageData.title,
          content: pageData.content,
          url
        })
      } finally {
        await page.close()
      }
    }

    // 导出为单页 HTML
    await exportToSinglePage({
      pages,
      output,
      ...options
    })
  } finally {
    await browser.close()
  }
}
