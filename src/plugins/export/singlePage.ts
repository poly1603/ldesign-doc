/**
 * 单页 HTML 导出功能
 * 将多个页面合并为单个 HTML 文件
 */

import type { HTMLConfig } from './index'

/**
 * 单页导出选项
 */
export interface SinglePageExportOptions extends HTMLConfig {
  /** 要导出的页面 */
  pages: PageContent[]
  /** 输出文件路径 */
  output: string
  /** 文档标题 */
  title?: string
  /** 基础 URL（用于修复相对链接） */
  baseUrl?: string
}

/**
 * 页面内容
 */
export interface PageContent {
  /** 页面标题 */
  title: string
  /** 页面内容（HTML） */
  content: string
  /** 页面 ID（用于锚点） */
  id?: string
  /** 页面 URL（原始 URL） */
  url?: string
}

/**
 * 导出为单页 HTML
 * 
 * @param options 单页导出选项
 * @returns Promise<void>
 * 
 * @example
 * ```ts
 * await exportToSinglePage({
 *   pages: [
 *     { title: 'Introduction', content: '<h1>Intro</h1><p>...</p>', id: 'intro' },
 *     { title: 'Getting Started', content: '<h1>Start</h1><p>...</p>', id: 'start' }
 *   ],
 *   output: './output/documentation.html',
 *   title: 'My Documentation',
 *   inlineStyles: true
 * })
 * ```
 */
export async function exportToSinglePage(options: SinglePageExportOptions): Promise<void> {
  const {
    pages,
    output,
    title = 'Documentation',
    inlineStyles = true,
    inlineImages = false,
    baseUrl
  } = options

  // 验证输入
  if (!pages || pages.length === 0) {
    throw new Error('At least one page is required for single-page export')
  }

  // 生成页面 ID
  const pagesWithIds = pages.map((page, index) => ({
    ...page,
    id: page.id || `page-${index + 1}`
  }))

  // 修复内部链接
  const processedPages = pagesWithIds.map(page => ({
    ...page,
    content: fixInternalLinks(page.content, pagesWithIds, baseUrl)
  }))

  // 生成目录
  const toc = generateTableOfContents(processedPages)

  // 生成完整 HTML
  const html = generateHTML({
    title,
    toc,
    pages: processedPages,
    inlineStyles,
    inlineImages
  })

  // 写入文件
  const { writeFileSync, mkdirSync } = await import('node:fs')
  const { dirname } = await import('node:path')

  // 确保输出目录存在
  mkdirSync(dirname(output), { recursive: true })

  // 写入 HTML
  writeFileSync(output, html, 'utf-8')
}

/**
 * 修复内部链接为锚点
 */
function fixInternalLinks(
  content: string,
  pages: (PageContent & { id: string })[],
  baseUrl?: string
): string {
  let fixed = content

  // 创建 URL 到 ID 的映射
  const urlToId = new Map<string, string>()
  pages.forEach(page => {
    if (page.url) {
      urlToId.set(page.url, page.id)
      // 也处理不带协议的 URL
      const withoutProtocol = page.url.replace(/^https?:\/\//, '')
      urlToId.set(withoutProtocol, page.id)
    }
  })

  // 替换内部链接为锚点
  fixed = fixed.replace(
    /<a\s+([^>]*?)href=["']([^"']+)["']([^>]*)>/gi,
    (match, before, href, after) => {
      // 检查是否是内部链接
      const cleanHref = href.replace(/^https?:\/\//, '')

      if (urlToId.has(href) || urlToId.has(cleanHref)) {
        const id = urlToId.get(href) || urlToId.get(cleanHref)
        return `<a ${before}href="#${id}"${after}>`
      }

      // 如果有 baseUrl，修复相对链接
      if (baseUrl && !href.startsWith('http') && !href.startsWith('#')) {
        const absoluteUrl = new URL(href, baseUrl).href
        return `<a ${before}href="${absoluteUrl}"${after}>`
      }

      return match
    }
  )

  return fixed
}

/**
 * 生成目录
 */
function generateTableOfContents(pages: (PageContent & { id: string })[]): string {
  const items = pages.map(page => {
    const title = escapeHTML(page.title)
    return `<li><a href="#${page.id}">${title}</a></li>`
  })

  return `
<nav class="toc">
  <h2>Table of Contents</h2>
  <ul>
    ${items.join('\n    ')}
  </ul>
</nav>
  `.trim()
}

/**
 * 生成完整 HTML 文档
 */
function generateHTML(options: {
  title: string
  toc: string
  pages: (PageContent & { id: string })[]
  inlineStyles: boolean
  inlineImages: boolean
}): string {
  const { title, toc, pages, inlineStyles } = options

  const styles = inlineStyles ? `<style>${getDefaultStyles()}</style>` : ''

  const pagesSections = pages.map(page => {
    const pageTitle = escapeHTML(page.title)
    return `
<section id="${page.id}" class="page-section">
  <h1 class="page-title">${pageTitle}</h1>
  <div class="page-content">
    ${page.content}
  </div>
</section>
    `.trim()
  })

  return `
<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${escapeHTML(title)}</title>
  ${styles}
</head>
<body>
  <div class="container">
    <header>
      <h1 class="doc-title">${escapeHTML(title)}</h1>
    </header>
    
    ${toc}
    
    <main class="content">
      ${pagesSections.join('\n\n      ')}
    </main>
    
    <footer>
      <p>Generated by @ldesign/doc</p>
    </footer>
  </div>
  
  <script>
    // 平滑滚动到锚点
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
      anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
          target.scrollIntoView({ behavior: 'smooth', block: 'start' });
        }
      });
    });
  </script>
</body>
</html>
  `.trim()
}

